class Contact:

    def __init__(self, firstname=None, lastname=None, nickname=None, company=None, email=None, bday=None, bmonth=None, byear=None):
        self.firstname = firstname
        self.lastname = lastname
        self.nickname = nickname
        self.company = company
        self.email = email
        self.bday = bday
        self.bmonth = bmonth
        self.byear = byear
